using namespace csv;

std::string base_name(std::string const & path)
{
    if(path.find("/") != std::string::npos) {
        return path.substr(path.find_last_of("/\\") + 1);
    }
    return path;
}

int main(int argc, char const *argv[])
{


    CLI::App app{"EvalMaxSAT Process Result"};

    std::string file;
    app.add_option("file", file, "Log file, format = (file \\t cost \\t time)*)")->check(CLI::ExistingFile)->required();
    CLI11_PARSE(app, argc, argv);

    CSVFormat format;
    format.delimiter('\t')
          .no_header();

    CSVReader reader(file, format);

    std::map<std::string, unsigned int> mapTime;
    std::map<std::string, unsigned long long> mapCost;
    for (auto& row: reader) {
        if(row.size() < 3)
            continue;

        auto it = row.begin();
        std::string file = base_name((it++)->get<std::string>());
        mapCost[file] = (it++)->get<unsigned long long>();
        mapTime[file] = (it++)->get<unsigned long>();
    }



    for(auto [file, cost]: reponsesWeighted) {
        if(mapTime.count(file)) {
            std::cout << file << "\t" << mapTime[file] << "\t" << (mapCost[file] != cost ?"ERREUR DU COUT":"") << std::endl;
        } else {
            std::cout << file << "\t?\t" << std::endl;
        }
    }



	return 0;
}



